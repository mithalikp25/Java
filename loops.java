//for loop
 
class loops{
    public static void main(String[] args){
        for(int i=0;i<=100;i++){
            System.out.println(i);
        }
    }
}


//while loop

class loops{
    public static void main(String[] args){
        int j=1;
        while(j<=10){
            System.out.println(j);
            j++;
        }
    }
}